import java.util.Calendar;

public class Client {
	// private members
	private int ID;
	private int port;
	private String name;
	
	// constructors
	public Client() { }
	public Client(int id)
	{
		this.ID = id;
	}
	public Client(int id, int port)
	{
		this.ID = id;
		this.port = port;
	}
	
	// accessor methods
	public int getID() 
	{ 
		return this.ID;
	}
	public void setID(int id)
	{
		this.ID = id;
	}
	public int getPort()
	{
		return port;
	}
	public void setPort(int port)
	{
		this.port = port;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	
	// utility methods
	public void printDate()
	{
		Calendar ca = Calendar.getInstance();
		System.out.println(ca.getTime().toString());
	}
}
